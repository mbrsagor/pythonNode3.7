number = 10

while number >= 0:
    print(number)
    number -= 1

print('\n\n')

# Conditional logic
count = 0
while count <= 5:
    if count == 3:
        break
    else:
        print(count)
    count += 1

print('\n\n')

num2 = 0
while num2 <= 12:
    if num2 == 5:
        continue
    else:
        print(num2)
    num2 += 1

print('\n\n')

